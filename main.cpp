using namespace std;
using namespace sf;

int main()
{
    RenderWindow window(VideoMode(1280, 720), "Fireboy and Watergirl");

    while (window.isOpen())
    {
        Event event;
        while (window.pollEvent(event))
        {
            if (event.type == Event::Closed) {
                window.close();
            }
            if (event.type == Event::KeyPressed && event.key.code == Keyboard::Escape) {
                window.close();
            }
        }

        window.clear();

        window.display();
    }

    return 0;
}